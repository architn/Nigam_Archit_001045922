    private void btnSearchSeatsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchSeatsActionPerformed
        // TODO add your handling code here:
        lblNoRecordsFound.setText("");
        DefaultTableModel model = (DefaultTableModel) tblMaximumCapacity.getModel();
        model.setRowCount(0);
        int specifiedMaxCapacity = Integer.parseInt(txtSearchBySeats.getText());
        
        for(CarProfile cp : carRecords.getCarRecords())
        {
            if(specifiedMaxCapacity == cp.getMaximumSeatingCapacity())
            {
            Object[] row = new Object[6];
            row[0] =  cp.getDriverName();
            row[1] =  cp.getCarBrand() + " " + cp.getCarModelNumber();
            row[2] = cp.getCarSerialNumber();
            row[3] = cp.getAvailabilityStatus();
            row[4] = cp.getCarLoctatedInCity();
            row[5] = cp.getMaximumSeatingCapacity();
            model.addRow(row);
            }
        }
        if(tblMaximumCapacity.getRowCount() == 0)
        {
            lblNoRecordsFound.setText(AppConstants.NoRecordsFound);
        }
    }//GEN-LAST:event_btnSearchSeatsActionPerformed


